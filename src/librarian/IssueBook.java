    public boolean checkValid(String callNo, String id, String contactNo, String date)
    {  
        int i;
         for (i=0; i<id.length(); i++)
         {
     if (id.charAt(i)>'9' || id.charAt(i)<'0' )
                {
                 JOptionPane.showMessageDialog(null, "Incorrect ID");
                  return  false;
                }}
    
    for (i=0; i< contactNo.length(); i++)
        {                  
                 if (contactNo.charAt(i) > '9' || contactNo.charAt(i) < '0')
                    {              
                    JOptionPane.showMessageDialog(null, "Incorrect contact number");
                    return false;
                    }
        }
     String tokens[]= date.split("-");
     if (Integer.parseInt(tokens[0])<2021 || Integer.parseInt(tokens[1])>12 || 
             Integer.parseInt(tokens[1])<1 || Integer.parseInt(tokens[2])<1 || Integer.parseInt(tokens[2])>31 )
     {
         JOptionPane.showMessageDialog(null, "Incorrect Date");
         return false;
     }
    if (callNo.charAt(0)>='A' && callNo.charAt(0)<='Z'&& callNo.charAt(1)>='@' && callNo.charAt(2)>='0' && callNo.charAt(2)<='9' ) return true;
            else
            {
               JOptionPane.showMessageDialog(null, "Incorrect CallNo.");
                return false;
            }
    }
