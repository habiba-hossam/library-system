    private void issueBooksActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_issueBooksActionPerformed
        // TODO add your handling code here:
         String bookCallNo = textcallNo.getText();
         String studentId = textStudentId.getText();
         String studentName = textName.getText();
         String studentContact = textContact.getText();
         String returnedDate=textReturned.getText();
        try {
            IssuedCommonTasks.readFiles();
            StudentsCommonTasks.readFiles();
            BooksCommonTasks.readFiles();
        } catch (FileNotFoundException ex) {
            Logger.getLogger(IssueBook.class.getName()).log(Level.SEVERE, null, ex);
        }
         int i; boolean check = false; 
         boolean found=false; int x = 0; int j; int n=0;
         if (checkValid(bookCallNo,studentId,studentContact,returnedDate))
         {
        for (i=0;i<(StudentsCommonTasks.count);i++)
        {
            if (StudentsCommonTasks.u[i].getId().equals(studentId) && StudentsCommonTasks.u[i].getUserName().equals(studentName))
            {
                found=true;            
                 for( j=0; j<IssuedCommonTasks.count; j++)
                     {  
                         if(IssuedCommonTasks.u[j].getStudentId().equals(studentId))
                                 {
                                         x++;
                                 }
                      }
                 break;
            }
        }
        for (i=0; i<BooksCommonTasks.count ; i++)
        {
            if (BooksCommonTasks.u[i].getCallNo().equals(bookCallNo))
            {
                n = i;
                check = true;
                break;
            }
        }
        if (Integer.parseInt(BooksCommonTasks.u[n].getQuantity() )== 0 ) JOptionPane.showMessageDialog(null, "The book is out of stock!!");
        else if( x >= 3) JOptionPane.showMessageDialog(null, "You issued the maximum number of books!!");
        else if (!check) JOptionPane.showMessageDialog(null, "The book is not existing!!");
        else if (!found) JOptionPane.showMessageDialog(null, "Wrong ID or NAME!!");
        else{
                    int quantityInt = Integer.parseInt(BooksCommonTasks.u[n].getQuantity());
                    int issuedInt = Integer.parseInt(BooksCommonTasks.u[n].getIssued());
                    quantityInt--;
                    issuedInt++;
                    String quantityRet = Integer.toString(quantityInt);
                    String issuedRet =  Integer.toString(issuedInt);
                    BooksCommonTasks.u[n].setQuantity(quantityRet);
                    BooksCommonTasks.u[n].setIssued(issuedRet);
                    IssuedCommonTasks.u[IssuedCommonTasks.count] = new IssuedUser(bookCallNo,studentId,studentName,studentContact,returnedDate);
                    IssuedCommonTasks.count++;
                     JOptionPane.showMessageDialog(null, "Book issued successfully!");
            try {
                 saveFile();
             } catch (IOException ex) {
                 Logger.getLogger(IssueBook.class.getName()).log(Level.SEVERE, null, ex);
             }
             try {
                 IssuedCommonTasks.writeFile();
             } catch (IOException ex) {
                 Logger.getLogger(IssueBook.class.getName()).log(Level.SEVERE, null, ex);
             }
             
         }
       
        LibrarianSection m = new LibrarianSection();
        this.hide();
        m.setVisible(true);}
        
    }//GEN-LAST:event_issueBooksActionPerformed

